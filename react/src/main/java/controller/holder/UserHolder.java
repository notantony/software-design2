package controller.holder;

public class UserHolder {
    public String id = "";
    public String name = "";
    public UserHolder() {}

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public void setId(String id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }
}
